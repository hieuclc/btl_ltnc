void Map::DrawMap(){
    int type = 0;
    ymap = 0;
    int x1 , x2;
    x1 = (x_start % 32) * -1;
    if (x1 == 0) x2 = x1 + Game::SCREEN_WIDTH;
    else x2 = x1 + Game::SCREEN_WIDTH + 32;

    SDL_Texture* object = TextureManager::LoadTexture("assets/pics.png");
    for (int i = 0; i < 15 * 32; i += 32) {
        xmap = x_start / 32;
        for (int j = x1; j < x2; j += 32){
            type = level_1[ymap][xmap];
            dest.x = j;
            dest.y = i;
            dest.w = dest.h = 32;
            if (type != 0) {
                TextureManager::Draw(object, pics[type - 1], dest);
            }
            xmap++;
        }
        ymap++;
    }
    SDL_DestroyTexture(object);
    object = NULL;
}



